
SELECT DISTINCT OS,Z.CODTRFPAI,KL.NUMEXEC  FROM KITEMORDEM K 
	INNER JOIN KORDEMCOMPL KL ON
	K.CODCOLIGADA=KL.CODCOLIGADA
	AND K.CODFILIAL=KL.CODFILIAL
	AND K.CODORDEM=KL.CODORDEM
	INNER JOIN FLUIG.DBO.Z_CRM_EX001005 Z ON
	Z.CODCOLIGADA=K.CODCOLIGADA
	AND Z.CODFILIAL=K.CODFILIAL
	AND Z.OS=K.CODCCUSTO COLLATE SQL_Latin1_General_CP1_CI_AI
	AND Z.CODIGOPRD=K.CODESTRUTURA COLLATE SQL_Latin1_General_CP1_CI_AI
	AND Z.EXECUCAO=KL.NUMEXEC
	LEFT JOIN KATVORDEMPROCESSO KP ON
	KP.CODCOLIGADA=K.CODCOLIGADA
	AND KP.CODFILIAL=K.CODFILIAL
	AND KP.CODORDEM=K.CODORDEM
	WHERE K.CODCCUSTO='3.07872.32.001' AND Z.CODTRFPAI IN ('1.51','1.52','1.54','1.55','1.56','1.67','1.68','1.69','1.71','1.74','1.75','1.76','1.78','1.79','1.93','1.94','1.95','1.96','1.98')
	ORDER BY CODTRFPAI,KL.NUMEXEC


